
//// DEPENDENCIES, COMPONENTS, STYLES ////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

import React, { Component } from 'react';

import CartItem from '../CartItem/CartItem'

import './ShoppingCart.css';






//// COMPONENT CLASS /////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

class ShoppingCart extends Component {



//// RENDER /////////////////////////////////////////////////////////////
  render() {

    console.log("ShoppingCart rendered")

    let cartItems = this.props.items.map((item, indx) => {
      return <CartItem  item={item} key={indx} addQuant={this.props.addQuant}
                        subQuant={this.props.subQuant}/>
    })

//// RETURN //////////////////////////////////////////////////////////////
    return (
      <div className="ShoppingCart">

        <h4>&nbsp; {this.props.totalItems} &nbsp;items in your cart</h4>

        {cartItems}

      </div>
    );
  }
}






//// EXPORT COMPONENT ////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

export default ShoppingCart;





